#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	const unsigned char	*s;
	unsigned char		*d;
	unsigned char		x;
	size_t				i;

	s = (const unsigned char *)src;
	d = (unsigned char *)dst;
	i = n;
	x = (unsigned char)c;
	while (i--)
		if ((*d++ = *s++) == x)
			return (d);
	return (NULL);
}
